def single_word(i):
    lattice = i[:-2]  # 获取数据
    word = i[-2:-1]  # 获取文本
    # print(word)
    try:
        # 进行编码
        utf = word.encode('utf-8')
        byte_utf = 0x00
        byte_utf |= utf[0] << 16
        byte_utf |= utf[1] << 8
        byte_utf |= utf[2]
        # 进行数据保存
        result = '{}:\n  [{}], #{}\n'.format(hex(byte_utf), lattice, word)
        print(result)

    except IndexError:
        print(word)

